        $(document).ready(function() {
            $("#form-delete-supplier").validate({
                rules: {},
                submitHandler: function(form) {
                    $.ajax({
                        url: form.action,
                        type: form.method,
                        data: $(form).serialize(),
                        success: function(response) {
                            if (response.status) {
                                // Reset form & error
                                $(form)[0].reset();
                                $('.error-text').text('');
                                $('#supplier_id').val('');

                                // Tutup modal langsung
